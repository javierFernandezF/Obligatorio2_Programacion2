    private void exportarReporteActionPerformed(java.awt.event.ActionEvent evt) {   
        //Intentar hacerlo con hasNextLine y nextLine()
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Guardar reporte de movimientos");
        if (chooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            String filename = file.getAbsolutePath();
        if (!filename.toLowerCase().endsWith(".csv")) {
            filename += ".csv";
            file = new File(filename);
        }
            try (PrintWriter pw = new PrintWriter(new FileWriter(file))) {
                pw.println("Mes,Área Origen,Área Destino,Empleado");
                DefaultTableModel model = (DefaultTableModel) tablaMovimientos.getModel();
                for (int i = 0; i < model.getRowCount(); i++) {
                    String mes = model.getValueAt(i, 0).toString();
                    String areaO = model.getValueAt(i, 1).toString();
                    String areaD = model.getValueAt(i, 2).toString();
                    String emp = model.getValueAt(i, 3).toString();
                    pw.println(mes + "," + areaO + "," + areaD + "," + emp);
                }
                pw.flush();
                JOptionPane.showMessageDialog(this, "Reporte exportado correctamente");
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this, "Error al exportar");
            }
        }
    }   
