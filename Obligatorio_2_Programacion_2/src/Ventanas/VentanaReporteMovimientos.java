    private void cargarFiltros() {
        // Empleados
        selectEmpleado.removeAllItems();
        selectEmpleado.addItem("Todos");
        for (Empleado emp : sistema.getListaPersonas().getEmpleadosOrdenados()) {
            selectEmpleado.addItem(emp.getNombre());
        }
        // Áreas
        selectArea.removeAllItems();
        selectArea.addItem("Todas");
        for (Area area : sistema.getListaAreas().getAreas()) {
            selectArea.addItem(area.getNombre());
        }
    }
